package model;

public class Passport {
    private Integer id;
    private Integer seria;
    private Integer nomer;
    private String vidachi;
    private String podrazdeleniya;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getSeria() {
        return seria;
    }

    public void setSeria(Integer seria) {
        this.seria = seria;
    }

    public Integer getNomer() {
        return nomer;
    }

    public void setNomer(Integer nomer) {
        this.nomer = nomer;
    }

    public String getVidachi() {
        return vidachi;
    }

    public void setVidachi(String vidachi) {
        this.vidachi = vidachi;
    }

    public String getPodrazdeleniya() {
        return podrazdeleniya;
    }

    public void setPodrazdeleniya(String podrazdeleniya) {
        this.podrazdeleniya = podrazdeleniya;
    }
    
    
}